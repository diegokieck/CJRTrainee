function abreboca(){
	
	document.getElementById("openmouth").style.display = "initial";
	document.getElementById('closedmouth').style.display = "block";
}

function fechaboca(){
	
	document.getElementById("openmouth").style.display = "none";
	document.getElementById('closedmouth').style.display = "initial";
}

function mensagem(){
	document.getElementById("mensagem").style.display = "initial";
}
